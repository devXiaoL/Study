#import "SystemInfoVC.h"
#import <sys/utsname.h>


static NSString *cellIdentifier = @"systemInfo";

@interface SystemInfoVC ()<UITableViewDataSource,UITableViewDelegate>

@property (nonatomic, strong) UITableView *tableView;

@end

@implementation SystemInfoVC

- (void)viewDidLoad {
    [super viewDidLoad];
    
    ///测试git提交
    
    self.tableView.rowHeight = 40;
    [self.view addSubview:self.tableView];
}

- (void)didReceiveMemoryWarning {
    [super didReceiveMemoryWarning];
    // Dispose of any resources that can be recreated.
}


#pragma mark - DataSource

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section{
    return 10;
}


- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath{
    
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:cellIdentifier];
    
    if (!cell) {
        cell = [[UITableViewCell alloc]initWithStyle:UITableViewCellStyleValue1 reuseIdentifier:cellIdentifier];
        cell.accessoryType = UITableViewCellAccessoryNone;
    }
    [self settingTableViewDataSource:tableView cell:cell indexPath:indexPath];
    
    return cell;
}

#pragma mark - Delegate
- (void)tableView:(UITableView *)tableView didSelectRowAtIndexPath:(NSIndexPath *)indexPath{
    [tableView deselectRowAtIndexPath:indexPath animated:YES];
}


- (void)settingTableViewDataSource:(UITableView *)tableView cell:(UITableViewCell *)cell indexPath:(NSIndexPath *)indexPath{
    
//    cell.textLabel.font = [UIFont systemFontOfSize:12];
//    cell.detailTextLabel.font = [UIFont systemFontOfSize:11];
    cell.detailTextLabel.textColor = [UIColor lightGrayColor];
    
    if (indexPath.row == 0) {
        cell.textLabel.text = @"系统";
        NSString *str = [NSString stringWithFormat:@"%@,%@", [[UIDevice currentDevice] systemName],
                          [[UIDevice currentDevice] localizedModel]];
        cell.detailTextLabel.text = str;
    }else
        if (indexPath.row == 1) {
            cell.textLabel.text = @"版本";
            cell.detailTextLabel.text = [[UIDevice currentDevice] systemVersion];
    }else
        if (indexPath.row == 2){
            cell.textLabel.text = @"型号";
            cell.detailTextLabel.text = [self iphoneType];
        }
    
    
}


- (NSString *)iphoneType{
